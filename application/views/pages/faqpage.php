					  <div class="panel-body"><b>I don’t know anybody who has had these diseases. Why does my baby need these vaccines?</b><br>
While a few of these diseases have virtually disappeared because of vaccination, outbreaks of measles and whooping cough still occur in the U.S. Even if some diseases do completely disappear in the U.S., they are common in other parts of the world and are just a plane ride away. If we stop vaccinating against these diseases, many more people will become infected. Vaccinating your child will keep him or her safe.</div>
