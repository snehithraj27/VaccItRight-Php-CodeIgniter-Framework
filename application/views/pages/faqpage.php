					  <div class="panel-body"><b>How many times do I need to bring my baby in for vaccinations?</b><br>
At least five visits are needed before age two, but the visits can be timed to coincide with well-child check-ups. Your baby should get the first vaccine (hepatitis B) at birth, while still in the hospital. Multiple visits during the first two years are necessary because there are 14 diseases your baby can be protected against, and most require two or more doses of vaccine for the best protection.</div>
