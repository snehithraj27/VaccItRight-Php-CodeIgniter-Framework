					  <div class="panel-body"><b>What diseases do vaccines protect against?</b><br>
Immunizing your baby with vaccines protects against serious diseases like measles, whooping cough, polio, meningococcal disease, tetanus, rotavirus, hepatitis A, hepatitis B, chickenpox, influenza, and more.</div>
